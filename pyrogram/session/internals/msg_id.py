import logging
import time
import os

log = logging.getLogger(__name__)


class MsgId:
    last_time = 0
    offset = 0

    def __new__(cls) -> int:
        """Generates a random long integer (8 bytes), which is optionally signed"""
        return int.from_bytes(os.urandom(8), signed=True, byteorder="little")
